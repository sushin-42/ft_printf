#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	r_count;

	i = 0;
	r_count = 0;
	if (dst == 0 || src == 0)
		return (0);
	while (src[r_count])
		r_count++;
	while (i + 1 < dstsize && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	if (dstsize)
		dst[i] = '\0';
	return (r_count);
}
